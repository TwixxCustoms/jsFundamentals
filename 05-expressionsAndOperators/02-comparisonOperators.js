//Comparison Operators

//Equal
'3' == 3;

//Strict Equal
3 === 3

//Not Equal
'3' != 4;

//Strict not equal
'3'!== 3

//Greater than
3 > 2;

//Less than
2 < 3;

//Greater than or equal to
3 >= 2;   //

//Less than or equal to
2 <= 3;

//And
2 && 3  //Must find both

//Or
2 || 3  //Can find either


//Assignment Operators

let x = 5;

//Addition Assignment
x += 1; //x = x + 1

//Subtraction Assignment
x -= 1; //x = x - 1

//Multiplication Assignment
x *= 1; //x = x * 1

//Division Assignment
x /= 1; //x = x / 1

//Remainder (or Modulus) Assignment        // 10 / 3 = 3 %1     // 12 / 5 = 2 %2
x %= 1 //x = x % 1

//Exponential Assignment
x **= 1 // x = x** 1